						document.getElementById('calculator-form').addEventListener('submit', async function(e) {
							e.preventDefault();
							const amount = document.getElementById('amount').value;

							const response = await fetch('calculate.php', {
								method: 'POST',
								headers: {
									'Content-Type': 'application/json'
								},
								body: JSON.stringify({ amount: amount })
							});

							const result = await response.json();

							// Update the results section with the calculated values
							document.getElementById('amount-kwacha').innerText = `K${result.amountKwacha}`;
							document.getElementById('transfer-charge').innerText = `K${result.transferCharge}`;
							document.getElementById('total-amount').innerText = `K${result.totalAmount}`;

							// Show the results section
							document.getElementById('results').style.display = 'block';
